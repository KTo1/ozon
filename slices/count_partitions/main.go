//## Вопрос
//На какое количество пар списков можно так разбить массив, чтобы соблюдались следующие условия:
//- Оба списка не должны быть пусты;
//- Разница между суммой значений первого списка и суммой значений второго списка будет четной.
//
//### Пример:
//Для массива [10, 18, 3, 7, 6] количество подходящих пар будет 4:
//
//[10], [18, 3, 7, 6]
//Обратите внимание, что в этом примере сумма элементов первого списка равна 10,
//а сумма элементов второго списка равна 18. Сумма элементов первого списка равна 3,
//а сумма элементов второго списка равна 7. Следовательно,
//сумма элементов первого списка и суммы элементов второго списка будут равны 10 + 18 = 28. Следовательно, число пар будет равно 4.
// Л + П = С
// П = С - Л
// по условию надо найти разницу между Л П
// Л - (С- Л) = 2Л - С
// и получается что четность зависит только от суммы, если сумма С не четная, то и нет таких разбиений
// а если сумма С четная то любое разбиение подойдет

package main

import "fmt"

// countPartitions возвращает количество способов разрезать nums на две непустые части
// так, чтобы разница сумм частей была чётной.
// Если общая сумма чётная, то разница чётна для любого разреза → len(nums)-1 вариантов.
// Если общая сумма нечётная, разница всегда нечётна → 0 вариантов.

func countPartitions(nums []int) int {
	total := 0
	for _, v := range nums {
		total += v
	}

	if total%2 == 0 {
		return len(nums) - 1
	}

	return 0
}

func main() {
	nums := []int{10, 18, 3, 7, 6}
	fmt.Println(countPartitions(nums)) // 4
}
